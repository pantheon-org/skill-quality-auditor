// TestConfigFlag_WiredThroughRealCLIParsing exercises the actual cobra flag
// declaration and PersistentPreRunE hook end to end (as opposed to calling
// resolveConfig directly), confirming "-c" placed before the subcommand, as
// documented, actually reaches resolveConfig via the real argv parse path.
func TestConfigFlag_WiredThroughRealCLIParsing(t *testing.T) {
	resetConfigFlags(t)
	root, key := makeEvalSkill(t, "skill-minimal", "domain", "cli-flag-skill")

	t.Chdir(t.TempDir())
	withUserConfigDir(t, t.TempDir())

	flagPath := filepath.Join(t.TempDir(), "flag-config.yaml")
	writeMarkerConfig(t, flagPath, "cli-flag-marker")

	rootCmd.SetOut(&strings.Builder{})
	rootCmd.SetErr(&strings.Builder{})
	rootCmd.SetArgs([]string{"-c", flagPath, "evaluate", "--repo-root", root, key})
	defer rootCmd.SetArgs(nil)

	if err := rootCmd.Execute(); err != nil {
		t.Fatalf("execute: %v", err)
	}
	if got := activeMarker(t); got != "cli-flag-marker" {
		t.Errorf("expected -c placed before the subcommand to reach resolveConfig, got %q", got)
	}
}
