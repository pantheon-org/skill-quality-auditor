// writeMarkerConfig writes a structurally valid scoring-patterns.yaml whose
// d1_knowledge_delta.beginner_signals is exactly [marker], so tests can tell
// which tier's file ended up active.
func writeMarkerConfig(t *testing.T, path, marker string) {
	t.Helper()
	content := "version: 1\n" +
		"patterns:\n" +
		"  d1_knowledge_delta:\n" +
		"    beginner_signals: [\"" + marker + "\"]\n" +
		"    expert_signals: [\"x\"]\n" +
		"  analysis_quality:\n" +
		"    hedge_words: [\"x\"]\n" +
		"    vague_words: [\"x\"]\n" +
		"    passive_patterns: [\"x\"]\n" +
		"  d6_freedom_calibration:\n" +
		"    when_not_to_use: [\"x\"]\n"
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		t.Fatalf("mkdir: %v", err)
	}
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatalf("write marker config: %v", err)
	}
}

// activeMarker returns the current active config's single beginner signal,
// for comparing against the marker written by writeMarkerConfig.
func activeMarker(t *testing.T) string {
	t.Helper()
	signals := patternconfig.Get().Patterns.D1KnowledgeDelta.BeginnerSignals
	if len(signals) == 0 {
		t.Fatal("active config has no beginner signals")
	}
	return signals[0]
}

// withUserConfigDir points the default config-directory tier at dir for the
// duration of the test, restoring the TestMain-installed stub afterward.
func withUserConfigDir(t *testing.T, dir string) {
	t.Helper()
	orig := userConfigDir
	userConfigDir = func() (string, error) { return dir, nil }
	t.Cleanup(func() { userConfigDir = orig })
}

func TestResolveConfig_FlagWinsOverCWDAndDefault(t *testing.T) {
	resetConfigFlags(t)

	cwdDir := t.TempDir()
	t.Chdir(cwdDir)
	writeMarkerConfig(t, filepath.Join(cwdDir, userConfigFileName), "cwd-marker")

	defaultDir := t.TempDir()
	withUserConfigDir(t, defaultDir)
	writeMarkerConfig(t, filepath.Join(defaultDir, userConfigDirName, userConfigFileName), "default-marker")

	flagPath := filepath.Join(t.TempDir(), "flag-config.yaml")
	writeMarkerConfig(t, flagPath, "flag-marker")
	configFlag = flagPath
	t.Cleanup(func() { configFlag = "" })

	if err := resolveConfig(false); err != nil {
		t.Fatalf("resolveConfig: %v", err)
	}
	if got := activeMarker(t); got != "flag-marker" {
		t.Errorf("expected the -c flag to win, got %q", got)
	}
}

