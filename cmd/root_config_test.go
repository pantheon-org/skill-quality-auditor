func TestResolveConfig_EmbeddedUnchanged_WhenAutoGenerationFails(t *testing.T) {
	if os.Geteuid() == 0 {
		t.Skip("running as root: permission bits are not enforced")
	}
	resetConfigFlags(t)

	t.Chdir(t.TempDir()) // no CWD file
	readOnlyParent := t.TempDir()
	if err := os.Chmod(readOnlyParent, 0o500); err != nil {
		t.Fatalf("chmod: %v", err)
	}
	t.Cleanup(func() { _ = os.Chmod(readOnlyParent, 0o755) })
	withUserConfigDir(t, filepath.Join(readOnlyParent, "unwritable"))

	if err := resolveConfig(false); err != nil {
		t.Fatalf("resolveConfig should never fail even if auto-generation fails: %v", err)
	}
	if got := activeMarker(t); got != "npm install" {
		t.Errorf("expected embedded default despite write failure, got %q", got)
	}
}

