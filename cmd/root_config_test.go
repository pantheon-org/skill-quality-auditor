func TestResolveConfig_DefaultPath_AutoGeneratedOnFirstRun_AndReusedOnSecondRun(t *testing.T) {
	resetConfigFlags(t)

	t.Chdir(t.TempDir()) // no CWD file
	defaultDir := t.TempDir()
	withUserConfigDir(t, defaultDir)
	generatedPath := filepath.Join(defaultDir, userConfigDirName, userConfigFileName)

	if _, err := os.Stat(generatedPath); !os.IsNotExist(err) {
		t.Fatalf("expected no file before first run, stat err: %v", err)
	}

	if err := resolveConfig(false); err != nil {
		t.Fatalf("resolveConfig (first run): %v", err)
	}
	firstInfo, err := os.Stat(generatedPath)
	if err != nil {
		t.Fatalf("expected auto-generated file after first run: %v", err)
	}
	if got := activeMarker(t); got != "npm install" {
		t.Errorf("expected embedded default active on first run, got %q", got)
	}

	if err := resolveConfig(false); err != nil {
		t.Fatalf("resolveConfig (second run): %v", err)
	}
	secondInfo, err := os.Stat(generatedPath)
	if err != nil {
		t.Fatalf("expected the generated file to persist: %v", err)
	}
	if !firstInfo.ModTime().Equal(secondInfo.ModTime()) {
		t.Error("expected the second run to reuse the generated file as-is, not rewrite it")
	}
	if got := activeMarker(t); got != "npm install" {
		t.Errorf("expected embedded default active on second run, got %q", got)
	}
}

